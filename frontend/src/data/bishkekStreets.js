function normalizeText(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/й/g, "и")
    .replace(/-/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function searchBishkekStreets(query, limit = 8) {
  const q = normalizeText(query);

  if (!q) return [];

  const scored = BISHKEK_STREETS.map((street) => {
    const all = [street.name, ...(street.aliases || [])].map(normalizeText);

    let score = 0;

    for (const value of all) {
      if (value === q) score = Math.max(score, 100);
      else if (value.startsWith(q)) score = Math.max(score, 80);
      else if (value.includes(q)) score = Math.max(score, 60);
    }

    return {
      ...street,
      score,
    };
  })
    .filter((item) => item.score > 0)
    .sort((a, b) => b.score - a.score || a.name.localeCompare(b.name, "ru"))
    .slice(0, limit)
    .map((item) => ({
      id: item.id,
      name: item.name,
      subtitle: "Бишкек, Кыргызстан",
      shortName: item.name,
      source: "local",
    }));

  return scored;
}